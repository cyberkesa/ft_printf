#include "libft.h"

void	ft_putendl_fd(char *s, int fd)
{
	char	end;
	size_t	i;

	end = '\n';
	if (s != NULL)
	{
		i = 0;
		while (s[i])
		{
			write(fd, &s[i], 1);
			i++;
		}
	}
	write(fd, &end, 1);
}
